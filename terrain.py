def populate_terrain(grid):
    grid = []
    for row in range(6):
        grid.append([])
        for column in range(6):
            grid[row].append(random.randint(0,2))
    return grid



 
# Define some colors
black = (0, 0, 0)
white = (255, 255, 255)
green = (135, 234, 89)
sand = (255, 255, 100)
shallowwater = (50, 220, 255)
forest = (44, 127, 47)
wall = (175, 175, 175)
 
# This sets the WIDTH and HEIGHT of each grid location
WIDTH = 15
HEIGHT = 15
 
MARGIN = 2


pygame.init()
 
WINDOW_SIZE = [818, 818]
screen = pygame.display.set_mode(WINDOW_SIZE)
 

pygame.display.set_caption("Array Backed Grid")
 
done = False

clock = pygame.time.Clock()
 
def draw_grid():
    screen.fill(black)
 
    for row in range(48):
        for column in range(48):
            color = green
            if grid[row][column] == 1:
                color = shallowwater
            elif grid[row][column] == 2:
                color = sand
            elif grid[row][column] == 3:
                color = forest
            elif grid[row][column] == 4:
                color = wall
            pygame.draw.rect(screen,
                             color,
                             [(MARGIN + WIDTH) * column + MARGIN,
                              (MARGIN + HEIGHT) * row + MARGIN,
                              WIDTH,
                              HEIGHT])

    clock.tick(60)
    pygame.display.flip()


while 1==1:
    draw_grid()

pygame.quit()
